# performing data download from Kaggle ----------------------------------------

# for this project, we are using the following Kaggle dataset:
# -> https://www.kaggle.com/gyani95/380000-lyrics-from-metrolyrics
#
# to make it work you will need to install kaggle public API:
#   -> https://github.com/Kaggle/kaggle-api#Installation
#
# and set up your API credentials.
#   -> https://github.com/Kaggle/kaggle-api#api-credentials
#
# make sure to save your API credentials at: C:\Users\<username>\.kaggle\kaggle.json
#
# following, the API command to download the dataset from Kaggle:
#     kaggle datasets download -d gyani95/380000-lyrics-from-metrolyrics

